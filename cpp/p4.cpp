// WAP to find size of fundamental data-types
#include <stdio.h>

main()
{
    int a;
    float b;
    char c;
    double d;
    printf("Size of int=%d bytes\n", sizeof(a));
    printf("Size of float=%d bytes\n", sizeof(b));
    printf("Size of char=%d bytes\n", sizeof(c));
    printf("Size of double=%d bytes\n", sizeof(d));
}

